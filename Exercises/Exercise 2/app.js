//Read 5 variables as user input and print each of the var types in the console
let name = prompt("What is your name?");
console.log(`The type of ${name} is: ${typeof(name)}`);

let lastName = prompt("What is your last name?");
console.log(`The type of ${lastName} is: ${typeof(lastName)}`);

let age = parseInt(prompt("How old are you?"));
console.log(`The type of ${age} is: ${typeof(age)}`);

let city = prompt("Where are you coming from?");
console.log(`The type of ${city} is: ${typeof(city)}`);

let postNumber = parseInt(prompt("What is the post number of your city?"));
console.log(`The type of ${postNumber} is: ${typeof(postNumber)}`);
